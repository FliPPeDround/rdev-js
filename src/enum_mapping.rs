use rdev::{Button, Key};

macro_rules! create_enum_mapping {
  ($js_enum:ident, $rust_enum:ident, [$($variant:ident),* $(,)?]) => {
      #[napi(js_name = $rust_enum)]
      pub enum $js_enum {
          $($variant),*
      }

      impl From<$js_enum> for $rust_enum {
          fn from(item: $js_enum) -> Self {
              match item {
                  $($js_enum::$variant => <$rust_enum>::$variant),*
              }
          }
      }
  };
}

